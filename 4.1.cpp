#include <iostream>
int main() {
 int a = 36, b = 12;
 std::cout << "Penjumlahan: a + b = " << (a + b) << std::endl;
 std::cout << "Pengurangan: a - b = " << (a - b) << std::endl;
 std::cout << "Perkalian: a * b = " << (a * b) << std::endl;
 std::cout << "Pembagian: a / b = " << (a / b) << std::endl;
 std::cout << "Modulus: a % b = " << (a % b) << std::endl;
 return 0;
}